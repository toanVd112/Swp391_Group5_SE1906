    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int page = 1;
        int recordsPerPage = 10;
        if (request.getParameter("page") != null) {
            page = Integer.parseInt(request.getParameter("page"));
        }

        String search = request.getParameter("search");
        String sort = request.getParameter("sort");

        RoomDAO dao = new RoomDAO();
        List<Room> listR = dao.getRoomsByPage(search, sort, (page - 1) * recordsPerPage, recordsPerPage);
        int totalRooms = dao.countRooms(search);
        int totalPages = (int) Math.ceil((double) totalRooms / recordsPerPage);

        request.setAttribute("listR", listR);
        request.setAttribute("currentPage", page);
        request.setAttribute("totalPages", totalPages);
        request.setAttribute("search", search);
        request.setAttribute("sort", sort);
        request.getRequestDispatcher("Manager/managerRoom.jsp").forward(request, response);

    }

